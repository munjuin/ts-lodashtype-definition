// 1. 개요
// 배열의 처음부터 지정된 개수(n)만큼의 요소를 담은 새로운 배열을 반환하는 take 함수를 구현합니다.

// 2. 목적

// 데이터 중 필요한 앞부분만 선별하여 표시할 때 사용합니다 (예: '최근 게시물 5개만 보기').
// 대량의 데이터에서 샘플을 추출하거나 페이지네이션의 일부 로직에서 활용됩니다.
// 3. 요구사항

// 첫 번째 인자로 대상 배열(array)을 받고, 두 번째 인자로 가져올 개수(n)를 받습니다.
// n의 기본값은 1로 설정합니다.
// n이 0 이하인 경우 빈 배열([])을 반환합니다.
// n이 배열의 길이보다 큰 경우 원본 배열의 전체 복사본을 반환합니다.
// 원본 배열을 수정하지 않는 불변성(Immutability)을 유지해야 합니다.
// 4. 핵심 로직

// Array.prototype.slice(0, n)을 사용하여 효율적으로 구현합니다.
// n이 음수이거나 0일 때의 예외 처리를 포함합니다

export function take<T>(array: T[], n: number = 1): T[]{
  if(n <= 0){
    return [];
  }
  return array.slice(0, n);
}